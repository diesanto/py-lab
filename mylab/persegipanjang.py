# Script hitung luas dan keliling persegi panjang
# Input panjang dan lebar
# Output luas dan kelilig

# Input panjang dan lebar
panjang = float(input('Masukan Panjang Persegi Panjang : '))
lebar = float(input('Masukan Lebar Persegi Panjang :'))

# Hitung luas dan keliling
luas = panjang * lebar
keliling = 2*(panjang + lebar)

# Tampilkan luas dan keliling
print('\nLuas Persegi Pajang : ', luas, '\n')
print('Keliling Persegi Panjang : ', keliling)
